package com.vg.model;

public class Bolsat {

    private int cod_bol;
    private String esp_bol;
    private String nom_emp;
    private String des_bol;

    public int getCod_bol() {
        return cod_bol;
    }

    public void setCod_bol(int cod_bol) {
        this.cod_bol = cod_bol;
    }

    public String getEsp_bol() {
        return esp_bol;
    }

    public void setEsp_bol(String esp_bol) {
        this.esp_bol = esp_bol;
    }

    public String getNom_emp() {
        return nom_emp;
    }

    public void setNom_emp(String nom_emp) {
        this.nom_emp = nom_emp;
    }

    public String getDes_bol() {
        return des_bol;
    }

    public void setDes_bol(String des_bol) {
        this.des_bol = des_bol;
    }
    
}
